def main():
    with open(SRC, newline="", encoding="utf-8-sig") as f:
        rows = list(csv.DictReader(f))
    translated = 0
    kept = 0
    for row in rows:
        t = T.get(row["id"])
        if t is not None:
            row["target"] = t
            translated += 1
        else:
            # 非名稱雜訊：保留原樣
            row["target"] = row["source"]
            kept += 1
    os.makedirs(os.path.dirname(DST), exist_ok=True)
    with open(DST, "w", newline="", encoding="utf-8-sig") as f:
        w = csv.DictWriter(f, fieldnames=["file", "id", "source", "target", "note"],
                           quoting=csv.QUOTE_ALL, escapechar="\\")
        w.writeheader()
        w.writerows(rows)
    print(f"寫入 {DST}：共 {len(rows)} 列（翻譯 {translated}、雜訊保留 {kept}）")

if __name__ == "__main__":
    main()
