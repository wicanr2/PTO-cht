FMT = re.compile(r"%[-+0-9.]*[a-zA-Z%]")

rows = []
with open(SRC, encoding="utf-8-sig", newline="") as f:
    for r in csv.DictReader(f):
        rows.append(r)

errors, out = [], []
for r in rows:
    off, src = r["offset"], r["source"]
    tgt, note = T.get(off, ("", "未處理"))
    if tgt:
        lb = len(tgt.encode("big5"))
        ls = len(src.encode("ascii"))
        if lb > ls:
            errors.append(f"{off}: target {lb}B > source {ls}B  {tgt!r}")
        sf, tf = sorted(FMT.findall(src)), sorted(FMT.findall(tgt))
        if sf != tf:
            errors.append(f"{off}: fmt mismatch src={sf} tgt={tf}")
        if ("&" in src) != ("&" in tgt):
            errors.append(f"{off}: & mismatch")
    out.append({"offset": off, "source": src, "target": tgt, "note": note})

if errors:
    print("ERRORS:")
    print("\n".join(errors))
    sys.exit(1)

import os
os.makedirs(os.path.dirname(DST), exist_ok=True)
