FMT = re.compile(r"%[-+0-9.]*[a-zA-Z%]")

def main():
    with open(SRC, newline="", encoding="utf-8-sig") as f:
        rows = list(csv.DictReader(f))
    out = []
    for r in rows:
        off, src = r["offset"], r["source"]
        tgt, note = T.get(off, ("", "未決定"))
        if tgt:
            sb = len(src.encode("ascii"))
            tb = len(tgt.encode("big5"))
            assert tb <= sb, f"{off}: {tb} > {sb} ({src!r} -> {tgt!r})"
            assert FMT.findall(src) == FMT.findall(tgt), f"{off}: fmt mismatch"
        out.append({"offset": off, "source": src, "target": tgt, "note": note})
    os.makedirs(os.path.dirname(DST), exist_ok=True)
    with open(DST, "w", newline="", encoding="utf-8-sig") as f:
        w = csv.DictWriter(f, fieldnames=["offset", "source", "target", "note"],
                           quoting=csv.QUOTE_ALL)
        w.writeheader()
        w.writerows(out)
    filled = sum(1 for r in out if r["target"])
    print(f"rows={len(out)} filled={filled} empty={len(out)-filled}")

if __name__ == "__main__":
    main()
