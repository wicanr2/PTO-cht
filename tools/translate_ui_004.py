FMT = re.compile(r"%[-+0-9.]*[a-zA-Z%]")

rows = []
with open(SRC, newline="", encoding="utf-8-sig") as f:
    for r in csv.DictReader(f):
        rows.append(r)

errors = []
out = []
for r in rows:
    off, src = r["offset"], r["source"]
    tgt, note = T.get(off, ("", "未處理"))
    if tgt:
        lb = len(tgt.encode("big5"))
        ls = len(src.encode("ascii"))
        if lb > ls:
            errors.append(f"OVER {off}: {lb}>{ls} {tgt!r}")
        sf, tf = sorted(FMT.findall(src)), sorted(FMT.findall(tgt))
        if sf != tf:
            errors.append(f"FMT {off}: {sf} != {tf}")
        if "&" in src and tgt.count("&") != 1:
            errors.append(f"AMP {off}")
    out.append({"offset": off, "source": src, "target": tgt, "note": note})

if errors:
    print("\n".join(errors))
    sys.exit(1)

with open(DST, "w", newline="", encoding="utf-8-sig") as f:
    w = csv.DictWriter(f, fieldnames=["offset", "source", "target", "note"],
                       quoting=csv.QUOTE_ALL)
    w.writeheader()
    w.writerows(out)

filled = sum(1 for o in out if o["target"])
print(f"OK rows={len(out)} filled={filled} empty={len(out)-filled}")
