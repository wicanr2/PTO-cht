FMT = re.compile(r"%[-+0-9.]*[a-zA-Z%]")

def fmts(s):
    return sorted(FMT.findall(s))

rows_out = []
errors = []
with open(SRC, encoding="utf-8-sig", newline="") as f:
    rd = csv.DictReader(f)
    src_rows = list(rd)

for r in src_rows:
    off, src = r["offset"], r["source"]
    tgt, note = T.get(off, ("", "未翻譯"))
    if tgt:
        # backslash escape: source contains literal backslash in T\S
        sb = len(src.encode("ascii"))
        tb = len(tgt.encode("big5"))
        if tb > sb:
            errors.append(f"{off}: target {tb}B > source {sb}B: {tgt!r}")
        if fmts(tgt) != fmts(src):
            errors.append(f"{off}: fmt mismatch src={fmts(src)} tgt={fmts(tgt)}")
        if ("\\" in src) != ("\\" in tgt):
            errors.append(f"{off}: backslash mismatch")
    rows_out.append({"offset": off, "source": src, "target": tgt, "note": note})

with open(DST, "w", encoding="utf-8-sig", newline="") as f:
    wr = csv.DictWriter(f, fieldnames=["offset", "source", "target", "note"],
                        quoting=csv.QUOTE_ALL)
    wr.writeheader()
    wr.writerows(rows_out)

filled = sum(1 for r in rows_out if r["target"])
