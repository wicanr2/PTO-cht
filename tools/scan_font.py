#!/usr/bin/env python3
"""在指定檔案中搜尋常見 VGA/CP437 8x16 / 8x8 字型簽章，以及通用 bitmap font 啟發式掃描。"""
import sys
import struct

# IBM VGA 8x16 glyphs (subset)
VGA16 = {
    'A': bytes([0x00,0x00,0x10,0x38,0x6C,0xC6,0xC6,0xFE,0xC6,0xC6,0xC6,0xC6,0xC6,0x00,0x00,0x00]),
    'B': bytes([0x00,0x00,0xFC,0x66,0x66,0x66,0x7C,0x66,0x66,0x66,0x66,0xFC,0x00,0x00,0x00,0x00]),
    '0': bytes([0x00,0x00,0x7C,0xC6,0xCE,0xDE,0xF6,0xE6,0xC6,0xC6,0x7C,0x00,0x00,0x00,0x00,0x00]),
}
# Classic 8x8 glyphs
F8 = {
    'A': bytes([0x18,0x3C,0x66,0x66,0x7E,0x66,0x66,0x00]),
    'B': bytes([0x7C,0x66,0x66,0x7C,0x66,0x66,0x7C,0x00]),
    '0': bytes([0x3C,0x66,0x6E,0x76,0x66,0x66,0x3C,0x00]),
}
F8b = {  # 另一種常見 8x8 (top row 0)
    'A': bytes([0x00,0x18,0x3C,0x66,0x66,0x7E,0x66,0x66]),
    '0': bytes([0x00,0x3C,0x66,0x6E,0x76,0x66,0x66,0x3C]),
}


def find(data, sig, label, min_hits=1):
    hits = []
    i = data.find(sig)
    while i != -1:
        hits.append(i)
        i = data.find(sig, i + 1)
    if hits:
        print(f'  {label}: {len(hits)} hits at {[hex(h) for h in hits[:8]]}')
    return hits


def main():
    for path in sys.argv[1:]:
        data = open(path, 'rb').read()
        print(f'== {path} ({len(data)} bytes)')
        # 連續 A+B 是強訊號
        find(data, VGA16['A'] + VGA16['B'], 'VGA8x16 A+B')
        find(data, F8['A'] + F8['B'], '8x8 A+B')
        find(data, F8b['A'], '8x8b A')
        find(data, VGA16['A'], 'VGA8x16 A')
        find(data, VGA16['0'], 'VGA8x16 0')
        find(data, F8['0'], '8x8 0')


if __name__ == '__main__':
    main()
