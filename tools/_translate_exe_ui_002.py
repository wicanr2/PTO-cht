    tgt = T.get(off, "")
    if tgt:
        if len(tgt.encode("big5")) > len(src.encode("ascii")):
            errors.append(f"{off} OVERLEN: {len(tgt.encode('big5'))} > {len(src)}")
        sf, tf = FMT.findall(src), FMT.findall(tgt)
        if sf != tf:
            errors.append(f"{off} FMT: {sf} != {tf}")
    out.append([off, src, tgt, note])

